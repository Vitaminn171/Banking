    private void setDataToTable(JSONArray jSONArray, DefaultTableModel model, String email) throws ParseException {
        for (int i = 0; i < jSONArray.length(); i++) {
            JSONObject jOBJ = jSONArray.getJSONObject(i);
            String total = null;

            String dateString = String.valueOf(jOBJ.get("date"));
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat dateFormat1 = new SimpleDateFormat("dd/MM/yyyy");
            Date date = dateFormat.parse(dateString);

            if (email.equals(jOBJ.get("sender"))) {
                total = " - " + formatter.format(jOBJ.get("total"));
            } else {
                total = " + " + formatter.format(jOBJ.get("total"));
            }
            Object[] row = {jOBJ.get("transaction_id").toString(),
                jOBJ.get("note").toString(),
                total, dateFormat1.format(date)};
            //add exam to each row
            model.addRow(row);
        }
    }

