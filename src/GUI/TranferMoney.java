    private void jButton_transferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_transferActionPerformed
        // TODO add your handling code here:
        JSONObject jsonSend = new JSONObject();
        if (jComboBox_email.getSelectedItem() != null
                && !jTextArea_note.getText().equals("")
                && Integer.parseInt(jTextField_amount.getText()) > 1000
                && Integer.parseInt(jTextField_amount.getText()) < money
                && jTextField_amount.getText().matches(regex)) {

            String receiver = String.valueOf(jComboBox_email.getSelectedItem());
            int amount = Integer.parseInt(jTextField_amount.getText());
            String note = String.valueOf(jTextArea_note.getText());
            int balance = money - amount;
            Date date = new Date();
            SimpleDateFormat dateFormatter = new SimpleDateFormat("yyyy-MM-dd");

            jsonSend.put("email", email);
            jsonSend.put("money", amount);
            jsonSend.put("note", note);
            jsonSend.put("receiver", receiver);
            jsonSend.put("balance", balance);
            jsonSend.put("date", dateFormatter.format(date));
            jsonSend.put("func", "transferMoney");
            String dataReceive = "";
            try {
                dataReceive = controller.SendReceiveData(jsonSend.toString());
            } catch (Exception ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            JSONObject json = new JSONObject(dataReceive);
            if(json.getBoolean("status")){
                JOptionPane.showMessageDialog(this, "Transfer Complete!.");
                try {
                    jsonAll.remove("money");
                    jsonAll.put("money", balance);
                    new Home(jsonAll).setVisible(true);
                    this.dispose();
                } catch (ParseException ex) {
                    Logger.getLogger(TranferMoney.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Wrong input type!.");
        }


    }//GEN-LAST:event_jButton_transferActionPerformed

