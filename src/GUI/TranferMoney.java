    private void getAllUser(String email) {
        jComboBox_email.removeAllItems();
        jComboBox_email.revalidate();
        jComboBox_email.repaint();

        JSONObject jsonSend = new JSONObject();
        jsonSend.put("email", email);
        jsonSend.put("func", "getAllUser");
        String dataReceive = "";
        try {
            dataReceive = controller.SendReceiveData(jsonSend.toString());
        } catch (Exception ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        JSONObject json = new JSONObject(dataReceive);
        JSONArray jsonArray = json.getJSONArray("userList");
        setDataComboBox(jsonArray);
    }

    private void setDataComboBox(JSONArray jsonArray) {
        for (int i = 0; i < jsonArray.length(); i++) {
            JSONObject jsonObj = jsonArray.getJSONObject(i);
            jComboBox_email.addItem(jsonObj.getString("email"));

        }
    }

